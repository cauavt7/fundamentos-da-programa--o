# 1. Operadores matemático
numero_um = 20
numero_dois = 15

soma = numero_um + numero_dois
print(f"A soma entre {numero_um} + {numero_dois} = {soma}")

subtracao = numero_um - numero_dois
print(f"a subtracao entre {numero_um} - {numero_dois} = {subtracao}")

multiplicacao= numero_dois * numero_dois
print(f"a multiplicaco entre {numero_um} * {numero_dois} = {multiplicacao}")

divisao = numero_dois / numero_dois
print(f"a divisao entre {numero_um} / {numero_dois} = {divisao}")

porcentagem = numero_um % numero_dois
print(f"porcentagem entre {numero_um} % {numero_dois} = {porcentagem}")
